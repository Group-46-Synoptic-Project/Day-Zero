// get all language buttons
const languageButtons = document.querySelectorAll('.languageButton');

// add click event listener to all buttons
languageButtons.forEach(button => {
    button.addEventListener('click', (event) => {
        // prevent default link behaviour
        event.preventDefault();

        // get the chosen language
        let language = button.getAttribute('value');

        // store the language in LocalStorage
        localStorage.setItem('language', language);

        // redirect to the new page
        window.location.href = button.getAttribute('href');
    });
});

function translatePage(){

    // get the chosen language from LocalStorage
    let language = localStorage.getItem('language');

    // get all elements that need to be translated
    let translatableElements = document.querySelectorAll('.translatable');
    let translatableButtons = document.querySelectorAll('.translatableButton');


    // iterate over each element
    translatableElements.forEach(element => {
        // get the translation key from the element's 'data-translate' attribute
        let translateKey = element.getAttribute('dataTranslate');

        // translate the content
        element.textContent = translations[language][translateKey];
    });

    translatableButtons.forEach(button => {
        // get the translation key from the button's 'data-translate-button' attribute
        let translateKey = button.getAttribute('dataTranslateButton');

        // translate the button text
        button.value = translations[language][translateKey];
    });

}

